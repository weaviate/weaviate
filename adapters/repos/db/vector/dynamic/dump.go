package dynamic

import (
	"fmt"
	"strings"
)

func (dynamic *dynamic) Dump(labels ...string) {
	if len(labels) > 0 {
		fmt.Printf("--------------------------------------------------\n")
		fmt.Printf("--  %s\n", strings.Join(labels, ", "))
	}
	fmt.Printf("--------------------------------------------------\n")
	fmt.Printf("ID: %s\n", dynamic.id)
	fmt.Printf("--------------------------------------------------\n")
}
