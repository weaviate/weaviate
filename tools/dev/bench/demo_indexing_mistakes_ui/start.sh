#!/usr/bin/env bash
# Start the demo UI's static HTTP server.
#
# The cluster URL is hardcoded in app.js, but the API key isn't. This
# script reads WCD_API_KEY from the environment, writes it into a
# generated config.js that the browser loads before app.js, then serves
# the directory on http://localhost:8089/.
#
# The generated config.js is gitignored so the token never ends up in
# the repository.
#
# Usage:
#   WCD_API_KEY=$(wcs --dev token) ./start.sh
#
# Or set WCD_API_KEY some other way and just run ./start.sh.

set -euo pipefail

cd "$(dirname "$0")"

if [[ -z "${WCD_API_KEY:-}" ]]; then
  echo "Error: WCD_API_KEY env var not set." >&2
  echo "Get a token with: wcs --dev token" >&2
  echo "Then run: WCD_API_KEY=\$(wcs --dev token) $0" >&2
  exit 1
fi

cat > config.js <<EOF
// Auto-generated by start.sh. Do not commit. Do not edit.
window.WCD_API_KEY = "${WCD_API_KEY}";
EOF

PORT="${PORT:-8089}"
echo "config.js written. Open http://localhost:${PORT}/"
exec python3 -m http.server "${PORT}"
